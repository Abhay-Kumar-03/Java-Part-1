public class Main {
    public static void main(String[] args) {
        //int
        int myNumber = 865;
        int yourNumber;
        yourNumber = 88;
        System.out.println(yourNumber);
        yourNumber = 69;
        System.out.println(yourNumber);

        // float
        float myFloat = 8.32f;
        System.out.println(myFloat);
        myFloat = 9;
        System.out.println(myFloat);

        // double
        double myDouble = 3.69674;
        System.out.println(myDouble);

        // boolean
        boolean isVeg = false;
        System.out.println(isVeg);

        // char
        char myCharacter = 'a';
        System.out.println(myCharacter);


        // string
        String wishes = "Good Morning Abhay";
        System.out.println(wishes);
    }
}